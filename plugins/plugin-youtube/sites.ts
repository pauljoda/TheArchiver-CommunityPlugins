/**
 * Extract clean domain names from URL patterns for use in UI dropdowns.
 * "*.youtube.com" → "youtube.com", "https://youtu.be" → "youtu.be"
 */
export function extractDomains(patterns: string[]): Array<{ label: string; value: string }> {
  const seen = new Set<string>();
  const result: Array<{ label: string; value: string }> = [];

  for (const pattern of patterns) {
    let domain: string;
    if (pattern.startsWith("*.")) {
      domain = pattern.slice(2);
    } else {
      try {
        domain = new URL(pattern).hostname;
      } catch {
        continue;
      }
    }
    // Deduplicate (e.g. www.youtube.com and youtube.com)
    if (seen.has(domain)) continue;
    seen.add(domain);
    result.push({ label: domain, value: domain });
  }

  return result.sort((a, b) => a.label.localeCompare(b.label));
}

/** Pre-computed domain options for settings UI */
export const siteOptions = extractDomains(urlPatterns);
